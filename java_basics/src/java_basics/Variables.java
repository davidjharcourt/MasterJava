package java_basics;

public class Variables {

	public static void main(String[] args) {
		
		int x; 
		x = 4;
		
		System.out.println(x);
		
		x = 23;
		
		System.out.println(x);
		
		String words;
		
		words = "knuckles";
		
		System.out.println(words);
		
		String wonky = "wonky wonky";
		
		System.out.println(wonky);

	}

}
